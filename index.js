// Function to update the comparison based on user selection
function updateComparison() {
    const selectors = ["mobile1", "mobile2", "mobile3"];
    selectors.forEach((selector, index) => {
        const selectedMobile = document.getElementById(selector).value;
        if (selectedMobile && mobileData[selectedMobile]) {
            const productCard = document.querySelectorAll(".product-card")[index];
            productCard.querySelector("img").src = mobileData[selectedMobile].imgSrc;
            const specs = productCard.querySelectorAll(".spec-item");
            mobileData[selectedMobile].specs.forEach((spec, i) => {
                specs[i].textContent = spec.name;
                specs[i].className = `spec-item ${spec.class}`;
            });
        }
    });
}

// fetch data from an API
async function fetchMobileData(mobile) {
    // fetching data from an API
    return new Promise((resolve) => {
        setTimeout(() => {
            resolve(mobileData[mobile]);
        }, 10); // Simulate network delay
    });
}

// automatically fetching and updating data when a mobile is selected
document.querySelectorAll('.mobile-selector').forEach(selector => {
    selector.addEventListener('change', async (event) => {
        const selectedValue = event.target.value;
        const mobileData = await fetchMobileData(selectedValue);
        const imgElement = event.target.closest('.product-card').querySelector('img');
        const specsElement = event.target.closest('.product-card').querySelector('.specs');

        if (mobileData) {
            imgElement.src = mobileData.imgSrc;
            specsElement.innerHTML = mobileData.specs.map(
                spec => `<div class="spec-item ${spec.class}">${spec.name}</div>`
            ).join("");
        } else {
            imgElement.src = "";
            specsElement.innerHTML = "";
        }
    });
});

